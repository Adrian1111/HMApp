using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DiamondApp
{
    public class SubBox
    {
       

        public string First { get; set; }
        public string Second { get; set; }
        public string Third { get; set; }
        
       

        

        public SubBox(string first, string second, string third)
        {
            this.Second = second;
            this.First = first;
            this.Third = third;
        }

        
    }
}


